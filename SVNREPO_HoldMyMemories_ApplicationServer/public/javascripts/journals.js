function journal_path(id1,type)
{

	
	switch(type)
	{
       
		case 'chapter':
                    window.location ='/chapter_journal/new/'+id1;
                    break;
        case 'subchapter':
                    window.location ='/sub_chap_journal/new/'+id1;
                    break;
        case 'gallery':
                    window.location ='/gallery_journal/new/'+id1;
                    break;
        case 'moment':
                    window.location ='/myphotos/write_journal/'+id1;
                    break;                            	

	}
	
	//document.form1.submit();
	
}
